package org.gridlab.gat;

/**
 * @author rob
 */
public class AdaptorCreationException extends Exception {

	/**
	 *  
	 */
	public AdaptorCreationException() {
		super();
	}

	/**
	 * @param arg0
	 */
	public AdaptorCreationException(String arg0) {
		super(arg0);
	}

	/**
	 * @param arg0
	 * @param arg1
	 */
	public AdaptorCreationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	/**
	 * @param arg0
	 */
	public AdaptorCreationException(Throwable arg0) {
		super(arg0);
	}

}